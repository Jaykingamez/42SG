int	ft_sqrt(int nb)
{
	int	root;

	root = 2;
	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (root <= (nb / 2) && root <= 46341)
	{
		if (root * root == nb)
			return (root);
		root++;
	}
	return (0);
}
